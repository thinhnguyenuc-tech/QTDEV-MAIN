let index = 0;
let isStopped = false;
let timeout = null;

module.exports.run = async function ({ api, event, args }) {
  if (args[0] === "stop") {
    isStopped = true;
    if (timeout) clearTimeout(timeout);
    return api.sendMessage("Lũ Bot Ghẻ, Đòi Đấu Với Bot Anh 🤣", event.threadID, event.messageID);
  }

  if (isStopped) isStopped = false;

  const send = () => {
    if (isStopped) return;
    api.sendMessage(list[index], event.threadID);
    index = (index + 1) % list.length;
    timeout = setTimeout(send, 1000);
  };

  send();
};
